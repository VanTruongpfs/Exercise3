package Exercise33;

public class Date {
	private int day;
	private int month;
	private int year;
/**
 * 
 *this is a Date constructor
 * 
 *example:
 * 	new Date(26,10,2023);
 * 	new Date(27,10,2023);
 * 	new Date(28,10,2023);
 * 	
 */
	public Date(int day, int month, int year) {
		this.day = day;
		this.month = month;
		this.year = year;
	}

}
